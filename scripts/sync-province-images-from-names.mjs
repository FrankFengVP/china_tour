/**
 * Regenerate province-city-images-data.mjs from Chinese names.
 * Enforces 8 unique image URLs per city page (hero + 4 attractions + 3 foods).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { ALL_PROVINCE_CITY_SLUGS } from "./province-config.mjs";
import { CITY_HERO_CANDIDATES } from "./image-library.mjs";
import {
  pickAttractionImage,
  pickFoodImage,
  pickUnique,
  imageKey,
} from "./province-image-matcher.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const zhDetail = JSON.parse(
  fs.readFileSync(path.join(ROOT, "locales", "city-detail-provinces-zh.json"), "utf8")
);

const PROVINCE_CITY_IMAGES = {};
const duplicateErrors = [];

for (const slug of ALL_PROVINCE_CITY_SLUGS) {
  const detail = zhDetail.cityDetail?.[slug];
  if (!detail) continue;

  const usedKeys = new Set();
  const heroCandidates = (CITY_HERO_CANDIDATES[slug] || []).map((u) =>
    u.replace(/w=\d+/, "w=1200")
  );
  const hero = pickUnique(heroCandidates, usedKeys);

  const attractions = (detail.attractions || []).map((a) =>
    pickAttractionImage(a.name, usedKeys)
  );

  const foods = (detail.foods || []).map((f, i) =>
    pickFoodImage(f.name, slug, i, usedKeys)
  );

  const all = [hero, ...attractions, ...foods];
  const keys = all.map(imageKey);
  if (new Set(keys).size !== keys.length) {
    duplicateErrors.push(slug);
  }

  PROVINCE_CITY_IMAGES[slug] = { hero, attractions, foods };
}

if (duplicateErrors.length) {
  console.error("Duplicate images still present in:", duplicateErrors.join(", "));
  process.exit(1);
}

const outPath = path.join(__dirname, "province-city-images-data.mjs");
const fileContent = `/** AUTO-GENERATED by sync-province-images-from-names.mjs — do not hand-edit */\nexport { img, dl } from "./image-library.mjs";\n\nexport const PROVINCE_CITY_IMAGES = ${JSON.stringify(PROVINCE_CITY_IMAGES, null, 2)};\n`;
fs.writeFileSync(outPath, fileContent);
console.log(`Generated ${Object.keys(PROVINCE_CITY_IMAGES).length} cities (8 unique URLs each)`);

const { execSync } = await import("child_process");
execSync("node scripts/apply-province-images.mjs", { cwd: ROOT, stdio: "inherit" });
execSync("node scripts/patch-city-heroes.mjs", { cwd: ROOT, stdio: "inherit" });
